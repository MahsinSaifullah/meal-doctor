class User {
	constructor(
		id,
		name,
		gender,
		age,
		height,
		weight,
		activityLevel,
		goal,
		goalCalories,
		email,
		date
	) {
		this.id = id;
		this.name = name;
		this.gender = gender;
		this.age = age;
		this.height = height;
		this.weight = weight;
		this.activityLevel = activityLevel;
		this.goal = goal;
		this.goalCalories = goalCalories;
		this.email = email;
		this.date = date;
	}
}

export default User;
